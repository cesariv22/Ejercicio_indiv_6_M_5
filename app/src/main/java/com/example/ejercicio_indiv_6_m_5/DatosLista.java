package com.example.ejercicio_indiv_6_m_5;

public class DatosLista {
    private final String url;
    private final String dato;

    public DatosLista(String url, String dato) {
        this.url = url;
        this.dato = dato;
    }
    public String getUrl() {
        return url;
    }
    public String getDato() {
        return dato;
    }
}
